# ======================== Data Provider Function ========================

def data_provider(args, flag):
    """
    Unified data provider function for all three tasks
    
    Args:
        args: Arguments containing dataset configuration
        flag: 'train', 'val', 'test', or 'pred'
    
    Returns:
        data_set: Dataset object
        data_loader: DataLoader object
    """
    
    # Handle classification task
    if args.task == 'classification':
        # Use the existing dataloader.py function
        from dataloader import get_datasets
        loaders = get_datasets(args.data_path, args)
        # Return consistent format: (dataset, dataloader)
        # For classification, we return the appropriate loader based on flag
        if flag == 'train':
            return None, loaders[0]  # train_loader
        elif flag == 'val':
            return None, loaders[1]  # val_loader  
        elif flag == 'test':
            return None, loaders[2]  # test_loader
        else:
            return None, loaders[0]  # default to train_loader
    
    # Handle anomaly detection task
    elif args.task == 'anomaly_detection':
        drop_last = False
        if flag == 'test':
            shuffle_flag = False
            batch_size = args.batch_size
        else:
            shuffle_flag = True
            batch_size = args.batch_size
        
        data_set = Dataset_Anomaly(
            root_path=args.root_path,
            win_size=args.seq_len,
            flag=flag,
        )
        
        data_loader = DataLoader(
            data_set,
            batch_size=batch_size,
            shuffle=shuffle_flag,
            num_workers=getattr(args, 'num_workers', 0),
            drop_last=drop_last
        )
        
        return data_set, data_loader
    
    # Handle forecasting task
    else:  # forecasting
        timeenc = 0 if args.embed != 'timeF' else 1
        Data = data_dict[args.data]
        percent = getattr(args, 'percent', 100)
        max_len = getattr(args, 'max_len', -1)
        train_all = getattr(args, 'train_all', False)
        
        if flag == 'test':
            shuffle_flag = False
            drop_last = True
            batch_size = args.batch_size
            freq = args.freq
        elif flag == 'pred':
            shuffle_flag = False
            drop_last = False
            batch_size = 1
            freq = args.freq
        elif flag == 'val':
            shuffle_flag = True
            drop_last = True
            batch_size = args.batch_size
            freq = args.freq
        else:  # train
            shuffle_flag = True
            drop_last = True
            batch_size = args.batch_size
            freq = args.freq

        # Handle different dataset types
        if args.data in ['ETTh1', 'ETTh2', 'ETTm1', 'ETTm2']:
            data_set = Data(
                root_path=args.root_path,
                data_path=args.data_file,
                flag=flag,
                size=[args.seq_len, args.label_len, args.pred_len],
                features=args.features,
                target=args.target,
                timeenc=timeenc,
                freq=freq,
                percent=percent,
                max_len=max_len,
                train_all=train_all
            )
        else:  # custom dataset
            data_set = Data(
                root_path=args.root_path,
                data_path=args.data_file,
                flag=flag,
                size=[args.seq_len, args.label_len, args.pred_len],
                features=args.features,
                target=args.target,
                timeenc=timeenc,
                freq=freq,
                percent=percent,
                max_len=max_len,
                train_all=train_all,
                seasonal_patterns=getattr(args, 'seasonal_patterns', None)
            )
        
        print(f"{flag}: {len(data_set)}")
        
        data_loader = DataLoader(
            data_set,
            batch_size=batch_size,
            shuffle=shuffle_flag,
            num_workers=getattr(args, 'num_workers', 0),
            drop_last=drop_last
        )

        return data_set, data_loader