class Dataset_Anomaly(Dataset):
    """Dataset for anomaly detection tasks"""
    def __init__(self, root_path, win_size, flag="train"):
        self.flag = flag
        self.win_size = win_size
        self.step = 1
        
        # Load data based on flag
        data_file = os.path.join(root_path, f"{flag}.csv")
        if os.path.exists(data_file):
            self.data = pd.read_csv(data_file).values.astype(np.float32)
        else:
            # Fallback to numpy files or other formats
            data_file = os.path.join(root_path, f"{flag}.npy")
            if os.path.exists(data_file):
                self.data = np.load(data_file).astype(np.float32)
            else:
                raise FileNotFoundError(f"No data file found for {flag} in {root_path}")
        
        print(f"Anomaly {flag} data shape: {self.data.shape}")

    def __len__(self):
        return (self.data.shape[0] - self.win_size) // self.step + 1

    def __getitem__(self, index):
        index = index * self.step
        return np.float32(self.data[index:index + self.win_size]), np.float32([0])  # dummy label


